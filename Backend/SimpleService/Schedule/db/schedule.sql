CREATE DATABASE IF NOT EXISTS schedule;

USE schedule;

CREATE TABLE Schedule (
    Schedule_ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    Staff_ID INT NOT NULL,
    Request_ID INT NOT NULL,
    Date DATE NOT NULL
);

